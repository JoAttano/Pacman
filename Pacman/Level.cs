        private Texture2D Tileset;
 
        
        public Level()
        {
            
        }
       
        public bool Collision(Vector2 position)
        {          
            int x = (28*(int)position.X/224);
            int y = (36*(int)position.Y/288);
            bool collide = false;
            
            if ((position.X <= 0) || (position.X >= 224))
                collide = true;
            else if (tableLevel[y, x] >= 1)
                collide = true;

            return collide;
        }
        
        public void Load(ContentManager Content)
        {
            pacman = new Pacman();
            pacman.Load(Content);

            Tileset = Content.Load<Texture2D>("TileMap/Border");

        }

        public void Update(GameTime gameTime, KeyboardState keyState)
        {
            pacman.Update(gameTime, keyState);
        }
       

        public void Draw(SpriteBatch spriteBatch)
        {
            Rectangle curveOneLine = new Rectangle(0, 0, 8, 8);
            Rectangle straightOneLine = new Rectangle(8, 0, 8, 8);
            Rectangle curveDoubleLine = new Rectangle(8, 0, 8, 8);
            Rectangle straightDoubleLine = new Rectangle(8, 8, 8, 8);

            float angle0 = 0f;
            float angle90 = 90f * 3.14f / 180f;
            float angle180 = 180f * 3.14f / 180f;
            float angle270 = 270f * 3.14f / 180f;

            spriteBatch.Begin();
            for (int y = 0; y < 36; y++)
            {
                for (int x = 0; x < 28; x++)
                {
                    if (tableLevel[y, x] != 0)
                    {
                        Rectangle source;
                        float angle;
                        int tile = tableLevel[y, x] - 1;


                        if (tile <= 4)
                        {
                            angle = angle0; 
                        }
                        else if (tile <= 8)
                        {
                            tile -= 4;
                            angle = angle90;     
                        }
                        else if (tile <= 12)
                        {
                            tile -= 8;
                            angle = angle180;
                        }
                        else
                        {
                            tile -= 12;
                            angle = angle270;
                        }
                        source = new Rectangle(tile * 8 - 8, 0, 8, 8);
                        
                        spriteBatch.Draw(Tileset, new Vector2(x * 8, y * 8), source, Color.White, angle, new Vector2(4, 4), 1f, SpriteEffects.None, 0f);
                        

                    }
                }

            }

            spriteBatch.End();

            pacman.Draw(spriteBatch);
        }
        

        public void Draw(SpriteBatch spriteBatch)
        {
            Rectangle curveOneLine = new Rectangle(0, 0, 8, 8);
            Rectangle straightOneLine = new Rectangle(8, 0, 8, 8);
            Rectangle curveDoubleLine = new Rectangle(8, 0, 8, 8);
            Rectangle straightDoubleLine = new Rectangle(8, 8, 8, 8);

            float angle0 = 0f;
            float angle90 = 90f * 3.14f / 180f;
            float angle180 = 180f * 3.14f / 180f;
            float angle270 = 270f * 3.14f / 180f;


            

            spriteBatch.Begin();
            for (int y = 0; y < 36; y++)
            {
                for(int x = 0; x < 28; x++)
                {
                    if(tableLevel[y,x] == 2)
                    {
                        spriteBatch.Draw(Tileset, new Vector2(x * 8, y * 8), straightDoubleLine, Color.White, angle0, new Vector2(4, 4), 1f, SpriteEffects.None, 0f);
                    }
                }

            }

            spriteBatch.End();

            pacman.Draw(spriteBatch);
        }
        

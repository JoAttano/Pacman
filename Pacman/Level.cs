        private Texture2D Tileset;
 
        
        public Level()
        {
            
        }
       
        public bool Collision(Vector2 position)
        {          
            int x = (28*(int)position.X/224);
            int y = (36*(int)position.Y/288);
            bool collide = false;
            Console.Write(" X = {0} Y = {1} ", x, y);
            if ((position.X <= 0) || (position.X >= 224))
                collide = true;
            else if (tableLevel[y, x] >= 1)
                collide = true;

            return collide;
        }
        
        public void Load(ContentManager Content)
        {
            pacman = new Pacman();
            pacman.Load(Content);

            Tileset = Content.Load<Texture2D>("Sprites/pacman/PacmanBorder");

        }

        public void Update(GameTime gameTime, KeyboardState keyState)
